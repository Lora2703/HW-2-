package com.syntax.class02;

public class Homework1 {

	public static void main(String[] args) {
		double num1 = 21.15;
		double num2 = 7.5;
		double sum = num1+num2;
		double sub = num1-num2;
		double mul = num1*num2;
		double div = num1/num2;
		System.out.println("The sum of 2 numbers "+num1+" and "+num2+" is equal to "+sum);
		System.out.println("The difference of 2 numbers "+num1+" and "+num2+" is equal to "+sub);
		System.out.println("The product of 2 numbers "+num1+" and "+num2+" is equal to "+mul);
		System.out.println("The quotient of 2 numbers "+num1+" and "+num2+" is equal to "+div);

	}
}
