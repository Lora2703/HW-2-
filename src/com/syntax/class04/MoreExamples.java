package com.syntax.class04;

public class MoreExamples {

	public static void main(String[] args) {
		
		boolean mothersDay=true;
		
		if(mothersDay==true) {
			System.out.println("Happy Mother's day to all Mom");
		}else {
			System.out.println("Hello");
		}
		System.out.println("-------------------------");

		boolean rain=false;
		if(rain) {
			System.out.println("I will take umbrella");
		}else {
			System.out.println("No need for umbrella");
		}
		
		System.out.println("-------------------------");
		
		boolean breakTime=true;
		
		if(breakTime) {		
			System.out.println("Let's take a break");
		}else {
			System.out.println("Let's continue the class");
		}
	}

}
